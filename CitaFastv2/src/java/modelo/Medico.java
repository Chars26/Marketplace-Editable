package modelo;

/**
 *
 * @author abrah
 */
public class Medico extends Usuario {
    private String nombreCompleto;
    private String especialidad;
    private String sede;

    public Medico(int idUsuario, String perfil, String correo, String nombreCompleto, String tipoDocumento, String numeroDocumento, String password) {
        super(idUsuario, perfil, correo, nombreCompleto, tipoDocumento, numeroDocumento, password);
    }

    /**
     * @return the nombreCompleto
     */
    public String getNombreCompleto() {
        return nombreCompleto;
    }

    /**
     * @param nombreCompleto the nombreCompleto to set
     */
    public void setNombreCompleto(String nombreCompleto) {
        this.nombreCompleto = nombreCompleto;
    }

    /**
     * @return the especialidad
     */
    public String getEspecialidad() {
        return especialidad;
    }

    /**
     * @param especialidad the especialidad to set
     */
    public void setEspecialidad(String especialidad) {
        this.especialidad = especialidad;
    }

    /**
     * @return the sede
     */
    public String getSede() {
        return sede;
    }

    /**
     * @param sede the sede to set
     */
    public void setSede(String sede) {
        this.sede = sede;
    }
    
}
